<template>
  <b-modal
    :id="modalId"
    ref="modal"
    size="lg"
    :title="modalName"
    lazy
    @show="reset()"
    @ok="$emit('create', newDataObjectReference)"
  >
    <b-form-group>
      <b-container>
        <b-row class="mb-3">
          <b-col cols="2"> Name </b-col>
          <b-col cols="8">
            <b-form-input
              v-model="newDataObjectReference.name"
              required
              placeholder="Name"
            >
            </b-form-input>
          </b-col>
        </b-row>

        <b-row class="mb-3">
          <b-col cols="2"> Referenced Data Object ID </b-col>
          <b-col cols="8">
            <b-form-input
              v-model="newDataObjectReference.referencedDataObjectId"
              required
              placeholder="please insert referenced data object id"
            >
            </b-form-input>
          </b-col>
        </b-row>

        <b-row class="mb-3">
          <b-col cols="2"> Relationship </b-col>
          <b-col cols="8">
            <b-form-input
              v-model="newDataObjectReference.relationship"
              required
              placeholder="please insert relationship"
            >
            </b-form-input>
          </b-col>
        </b-row>
      </b-container>
    </b-form-group>
  </b-modal>
</template>

<script lang="ts">
import { DataObjectReference } from "@dlr-shepard/shepard-client";
import Vue from "vue";

interface DataObjectReferenceModelData {
  newDataObjectReference: DataObjectReference;
}

function initialState(): DataObjectReferenceModelData {
  return {
    newDataObjectReference: {
      name: "",
      referencedDataObjectId: 0,
      relationship: "",
    },
  };
}

export default Vue.extend({
  props: {
    modalId: {
      type: String,
      default: "DataObjectReferenceModal",
    },
    modalName: {
      type: String,
      default: "DataObjectReferenceModal",
    },
    currentCollectionId: {
      type: Number,
      required: true,
    },
    currentDataObjectId: {
      type: Number,
      required: true,
    },
  },
  data() {
    return initialState();
  },
  methods: {
    reset() {
      Object.assign(this.$data, initialState());
    },
  },
});
</script>
